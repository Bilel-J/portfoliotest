    <h1 id="titreprincipal">15. Javascript : les boucles et les conditions</h1>
    <h2>Les boucles</h2>
    <p>Les boucles permettent de répéter des actions simplement et rapidement. Une boucle peut être vue comme une version informatique de « copier N lignes » ou de « faire X fois quelque chose ».</p>
    <p>Il y a différents types de boucles mais elles se ressemblent toutes au sens où elles répètent une action un certain nombre de fois (ce nombre peut éventuellement être zéro). Les différents types de boucles permettent d'utiliser différentes façon de commencer et de terminer une boucle. Chaque type de boucle pourra être utilisé en fonction de la situation et du problème que l'on cherche à résoudre.</p>
    <h2>Les conditions</h2>
    <p>Une condition est un ensemble de règles qui peut interrompre ou modifier l'exécution normale du code, selon que la condition est remplie ou non.</p>
    <p>Une instruction, ou un ensemble d'instructions, est démarrée si une condition spécifique est remplie. Dans le cas contraire, d'autres instructions sont exécutées. Il est également possible de répéter l'exécution d'une instruction, ou ensemble d'instructions, tant qu'une condition n'est pas encore remplie.</p>
